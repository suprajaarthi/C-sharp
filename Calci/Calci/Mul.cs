public class Multiply  
{

    public  int Multiplication(int num1, int num2)
    {
       return num1*num2;

    }
    public  double Multiplication(double num1, double num2)
    {
       return num1*num2;

    }
     public long Multiplication(long num1, long num2)
    {
       return num1*num2;

    }
  
}